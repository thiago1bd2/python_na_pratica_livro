'''
  Exercício 26 - Crie uma variável com valor
  inicial 0, enquanto o valor dessa variável for igual ou
  menor que 10, exiba em tela o próprio valor. A cada execução
  a mesma deve ter seu valor atualizado, incrementando em 1 unidade.
'''

numero = 0

while numero <= 10:
    print(f'Valor agora é {numero}')
    numero += 1